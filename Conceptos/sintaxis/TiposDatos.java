package sintaxis;

public class TiposDatos {
    public static void main(String[] args) {
        // Tipos de datos y variables

        String mensaje = "¡Hola, Mundo!"; // Declaración de una variable de tipo String
        int numero = 10; // Declaración de una variable entera
        double decimal = 3.14; // Declaración de una variable de tipo double
        boolean esVerdadero = true; // Declaración de una variable booleana
        
        System.out.println("String: "+ mensaje + " Numero entero: " + numero +
        " Numero decimal: " + decimal + " Valor Boleano: " + esVerdadero);
    }
}
